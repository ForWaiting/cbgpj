#值得推荐分类
class WorthWatching(models.Model):
    title = models.CharField(max_length=30,verbose_name='标题')
    video_url = models.URLField(verbose_name='视频地址')
    img = models.ImageField(upload_to='mycbg/worth_watch',verbose_name='配图')

    def __str__(self):
        return self.title

    class Meta:
        db_table = "推荐视频"  # 表名
        verbose_name = "值得看"
        verbose_name_plural = "值得看"

class SpecialRecommend(models.Model):
    title = models.CharField(max_length=10,verbose_name='标题')
    sub_title = models.CharField(max_length=20,verbose_name='分类标题')
    icon = models.ImageField(upload_to='mycbg/special_icon',verbose_name='图标')

    def __str__(self):
        return self.title

    class Meta:
        db_table = "专题分类"  # 表名
        verbose_name = "专题推荐"
        verbose_name_plural = "专题推荐"

class Highlights(models.Model):
    name = models.CharField(max_length=10,verbose_name='角色亮点')
    rolelists = models.ForeignKey('RoleLists', on_delete=models.CASCADE, verbose_name='角色亮点', related_name='highlights')
    def __str__(self):
        return self.name

class RoleLists(models.Model):
    icon = models.ImageField(upload_to='mycbg/role_icon',verbose_name='角色图标')
    format_name = models.CharField(max_length=10,verbose_name='角色门派')
    equip_level = models.PositiveIntegerField(default=0,verbose_name='等级')
    area_name = models.CharField(max_length=10,verbose_name='区服')
    server_name = models.CharField(max_length=10,verbose_name='服务器')
    desc_sumup_short = models.PositiveIntegerField(default=0,verbose_name='评分')
    price = models.PositiveIntegerField(default=0,verbose_name='售价')
    collect_num = models.PositiveIntegerField(default=0,verbose_name='收藏数')
    ic_sys = models.ImageField(upload_to='mycbg/sys_icon',verbose_name='系统图标')
    sale_data = models.DateField(verbose_name='销售日期')

    class Meta:
        db_table = "角色表"  # 表名
        verbose_name = "角色列表"
        verbose_name_plural = "角色列表"
